package exo3;

public class Couple {
    private String s;
    private int weight;

    public Couple(String s, int weight) {
        this.s = s;
        this.weight = weight;
    }

    public String getS() {
        return s;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
